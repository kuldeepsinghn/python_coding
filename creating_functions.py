def sum_two_numbers(num_one, num_two):
    sum = num_one + num_two
    return sum


print('Sum of two numbers: ', sum_two_numbers(1, 9))


def calculate_age(current_year, birth_year):
    age = current_year - birth_year
    return age


print(calculate_age(2022, 2003))


def add_ten(num):
    ten = 10
    return num + ten


print(add_ten(90))
